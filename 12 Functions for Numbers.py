import math

print(abs(-5))  # 5
print(abs(5))  # 5

print(pow(2, 10))  # 1024

print(min(4, 2, 1, -10, 100, -7))  # -10
print(max(4, 2, 1, -10, 100, -7, 1000))  # 1000

# x.5, the values will be rounded up if the roundup value is an even number
# otherwise, it will be rounded down.
print(round(2.5))  # 2
print(round(3.5))  # 4

print(int(3.0))  # 3
print(float(3))  # 3.0
print(str(3) + str(3))  # 33
print(3 + 3)  # 6

print(math.e)  # 2.718281828459045
print(math.pi)  # 3.141592653589793

print(math.floor(4.9999))  # 4
print(math.ceil(4.0000001))  # 5
print(math.sqrt(36))  # 6.0
