x = [4, 2, 3, 1]
y = sorted(x)
print(x)  # [4, 2, 3, 1]
print(y)  # [1, 2, 3, 4]

# tuple
x = (4, 2, 3, 1)
y = sorted(x)

print(x)  # (4, 2, 3, 1)
print(y)  # [1, 2, 3, 4]

# dict keys
x = {"name": "Wilson", "age": 25}
y = sorted(x)

print(x)  # {'name': 'Wilson', 'age': 25}
print(y)  # ['age', 'name']

# string
x = "How are you doing today?"
y = sorted(x)
print(x)
print(y)
